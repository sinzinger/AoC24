import networkx as nx


def parse_input(file_path):
    """
    Reads the maze input from a file and returns the lines as a list of strings.
    """
    with open(file_path, "r") as f:
        return f.read().strip().split("\n")


def build_graph(lines):
    """
    Builds a directed graph (DiGraph) from the maze input.

    Parameters:
        lines (list of str): The maze input as a list of strings.

    Returns:
        nx.DiGraph: The graph representing the maze.
        tuple: The start node (position and initial direction).
    """
    graph = nx.DiGraph()
    directions = (1, -1, 1j, -1j)  # right, left, down, up
    start, end = None, None

    # Parse the maze and add nodes
    for i, line in enumerate(lines):
        for j, char in enumerate(line):
            if char == "#":
                continue  # Skip walls
            position = i + 1j * j
            if char == "S":
                start = (position, 1j)  # Start node with initial direction (down)
            if char == "E":
                end = position  # End position
            for direction in directions:
                graph.add_node((position, direction))

    # Add edges for movements and rotations
    for position, direction in graph.nodes:
        # Movement edges (forward)
        next_position = position + direction
        if (next_position, direction) in graph.nodes:
            graph.add_edge((position, direction), (next_position, direction), weight=1)

        # Rotation edges (90 degrees left or right)
        for rotation in (-1j, 1j):
            rotated_direction = direction * rotation
            graph.add_edge((position, direction), (position, rotated_direction), weight=1000)

    # Add edges from end position to "end" node
    for direction in directions:
        graph.add_edge((end, direction), "end", weight=0)

    return graph, start


def solve_part_1(graph, start):
    """
    Solves Part 1: Finds the shortest path length (cost) from start to end.

    Parameters:
        graph (nx.DiGraph): The graph representing the maze.
        start (tuple): The start node (position and initial direction).

    Returns:
        int: The cost of the shortest path.
    """
    return nx.shortest_path_length(graph, start, "end", weight="weight")


def solve_part_2(graph, start):
    """
    Solves Part 2: Finds the number of unique positions visited across all shortest paths.

    Parameters:
        graph (nx.DiGraph): The graph representing the maze.
        start (tuple): The start node (position and initial direction).

    Returns:
        int: The count of unique positions visited across all shortest paths.
    """
    all_paths = nx.all_shortest_paths(graph, start, "end", weight="weight")
    unique_positions = {
        position
        for path in all_paths
        for position, _ in path[:-1]  # Exclude the final "end" node
    }
    return len(unique_positions)


def main():
    maze = [line.strip() for line in open("./day_16/day_16_input.txt", "r").readlines()]
    graph, start = build_graph(maze)

    # Solve Part 1 and Part 2
    part_1_result = solve_part_1(graph, start)
    part_2_result = solve_part_2(graph, start)

    # Print results
    print(f"Part 1: Shortest path cost = {part_1_result}")
    print(f"Part 2: Unique positions visited = {part_2_result}")


if __name__ == "__main__":
    main()